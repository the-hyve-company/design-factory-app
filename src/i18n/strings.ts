// ─── Pseudo-locale `xx` (debug only) ──────────────────────────────────
//
// Wraps every PT value in angle brackets ⟨…⟩ so devs can flip language to
// `xx` and visually inspect coverage:
//   · String shows up as `⟨Salvar⟩` → translated through the i18n table
//     (markers visible).
//   · String shows up as `Save` (no markers) → hardcoded JSX literal,
//     gap to fix.
//
// Build is automatic from PT; never edit by hand. Format placeholders
// like `{0}` are preserved INSIDE the markers so format() still works.
const XX: Strings = {};
for (const k of Object.keys(PT)) {
  XX[k] = `⟨${PT[k]}⟩`;
}

export const STRINGS: Record<Lang, Strings> = {
  pt: PT,
  en: EN,
  xx: XX,
};

/** Format a string with positional `{0}`, `{1}`, … placeholders. */
export function format(template: string, ...args: Array<string | number>): string {
  return template.replace(/\{(\d+)\}/g, (_match, i) => {
    const idx = Number(i);
    return idx < args.length ? String(args[idx]) : `{${i}}`;
  });
}
